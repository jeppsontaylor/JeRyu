struct TestRoutingInference {
    tags: Vec<String>,
    risk_class: String,
    timeout_secs: u64,
    rationale: Vec<String>,
}

fn infer_test_tags(command: &str) -> Vec<String> {
    infer_test_routing(command).tags
}

fn infer_test_routing(command: &str) -> TestRoutingInference {
    let lower = command.to_ascii_lowercase();

    let untrusted_patterns = [
        "security",
        "chaos",
        "ip_exfiltration",
        "ip guard",
        "ip-guard",
        "sandbox",
        "fuzz",
    ];
    if untrusted_patterns
        .iter()
        .any(|pattern| lower.contains(pattern))
    {
        return TestRoutingInference {
            tags: vec!["untrusted".to_string()],
            risk_class: "untrusted".to_string(),
            timeout_secs: 1800,
            rationale: vec!["matched untrusted/risky command pattern".to_string()],
        };
    }

    let build_patterns = [
        "deployctl",
        "payloadctl",
        "build-local",
        "publish-rc",
        "cargo check",
        "cargo build",
        "cargo nextest",
        "cargo test --workspace",
        "cargo test -p veox-deploy",
        "cargo test -p veox-bootstrap",
        "cargo test -p veox-rc-gate",
        "cargo test -p cargo-vrc",
        "cargo test -p cargo-aer",
        "cargo test -p cargo-witness",
        "cargo test -p nht",
        "test-local-built",
        "test-local-rc",
        "deploy-canary",
    ];
    if build_patterns.iter().any(|pattern| lower.contains(pattern)) {
        return TestRoutingInference {
            tags: vec!["build".to_string()],
            risk_class: "build".to_string(),
            timeout_secs: 1200,
            rationale: vec!["matched build-heavy command pattern".to_string()],
        };
    }

    TestRoutingInference {
        tags: vec!["default".to_string()],
        risk_class: "default".to_string(),
        timeout_secs: max(600, TestRunOpts::default().timeout_secs),
        rationale: vec!["no heavy/risky pattern matched; using default runner".to_string()],
    }
}

#[path = "test_runner_runtime.rs"]
mod runtime;
pub async fn run_test(
    db: &crate::state::Db,
    client: &crate::gitlab_client::GitlabClient,
    opts: &TestRunOpts,
) -> Result<TestRunResult> {
    runtime::run_test(db, client, opts).await
}

pub(crate) async fn wait_for_test_result(
    client: &crate::gitlab_client::GitlabClient,
    project_id: i64,
    pipeline_id: i64,
    job_name: &str,
    timeout_secs: u64,
) -> Result<TestRunResult> {
    runtime::wait_for_test_result(client, project_id, pipeline_id, job_name, timeout_secs).await
}

#[path = "test_runner_job_ops.rs"]
mod job_ops;
pub async fn run_test_batch(
    db: &crate::state::Db,
    client: &crate::gitlab_client::GitlabClient,
    opts: &TestBatchOpts,
) -> Result<Vec<TestRunResult>> {
    job_ops::run_test_batch(db, client, opts).await
}

pub async fn requeue_job_by_name(
    client: &crate::gitlab_client::GitlabClient,
    project_id: i64,
    pipeline_id: i64,
    job_name: &str,
) -> Result<TestRunResult> {
    job_ops::requeue_job_by_name(client, project_id, pipeline_id, job_name).await
}

pub async fn pipeline_results(
    client: &crate::gitlab_client::GitlabClient,
    project_id: i64,
    pipeline_id: i64,
) -> Result<Vec<TestRunResult>> {
    job_ops::pipeline_results(client, project_id, pipeline_id).await
}

